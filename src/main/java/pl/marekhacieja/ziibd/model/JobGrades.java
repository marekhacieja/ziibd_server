package pl.marekhacieja.ziibd.model;

public class JobGrades {

    private String gradeLevel;
    private String lowestSal;
    private String highestSal;

    public String getGradeLevel() {
        return gradeLevel;
    }

    public void setGradeLevel(String gradeLevel) {
        this.gradeLevel = gradeLevel;
    }

    public String getLowestSal() {
        return lowestSal;
    }

    public void setLowestSal(String lowestSal) {
        this.lowestSal = lowestSal;
    }

    public String getHighestSal() {
        return highestSal;
    }

    public void setHighestSal(String highestSal) {
        this.highestSal = highestSal;
    }

}
